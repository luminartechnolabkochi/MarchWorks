class Parent:

    def phone(self):

        print("redmi15pro")


    def car(self):

        print("swift")


class Child(Parent):

    def cycle(self):

        print("hero")

    def phone(self):

        print("iphone16")

  

child_instance=Child()

child_instance.cycle()

child_instance.phone()

child_instance.car()
# method overriding
# this concept allows a child class to provide a specific implimentation of a method that is already defined in parent class
